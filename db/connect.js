// IMPORT 'dotenv' TO ALLOW USE OF ENVIRONMENT VARIABLES
const dotenv = require('dotenv');
dotenv.config(); 

// IMPORT MONGO CLIENT FUNCTIONALITY - MUST INSTALL MONGO PACKAGE PRIOR TO USING (npm install mongodb)
const MongoClient = require('mongodb').MongoClient;

let _db;

const initDb = (callback) => {
  if (_db) {
    console.log('Db is already initialized!');
    return callback(null, _db);
  }
  MongoClient.connect(process.env.URI)
    .then((client) => {
      _db = client;
      callback(null, _db);
    })
    .catch((err) => {
      callback(err);
    });
};

const getDb = () => {
  if (!_db) {
    throw Error('Db not initialized');
  }
  return _db;
};

module.exports = { initDb, getDb };